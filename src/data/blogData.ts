export const getFeaturedPosts = (count: number = 3): BlogPost[] => {
  return blogPosts.slice(0, count);
};

export const getRecentPosts = (count: number = 4): BlogPost[] => {
  return [...blogPosts].sort((a, b) => {
    const dateA = new Date(a.date).getTime();
    const dateB = new Date(b.date).getTime();
    return dateB - dateA;
  }).slice(0, count);
};

export const getPostBySlug = (slug: string): BlogPost | undefined => {
  return blogPosts.find(post => post.slug === slug);
};

export const getRelatedPosts = (currentPostId: string, count: number = 3): BlogPost[] => {
  const currentPost = blogPosts.find(post => post.id === currentPostId);
  if (!currentPost) return [];
  
  const related = blogPosts
    .filter(post => post.id !== currentPostId)
    .map(post => {
      let relevanceScore = 0;
      
      if (post.category === currentPost.category) {
        relevanceScore += 5;
      }
      
      const matchingTags = post.tags.filter(tag => currentPost.tags.includes(tag));
      relevanceScore += matchingTags.length * 2;
      
      return { post, relevanceScore };
    })
    .filter(item => item.relevanceScore > 0)
    .sort((a, b) => b.relevanceScore - a.relevanceScore)
    .map(item => item.post)
    .slice(0, count);
  
  if (related.length < count) {
    const moreNeeded = count - related.length;
    const existingIds = new Set([...related.map(p => p.id), currentPostId]);
    
    const additional = blogPosts
      .filter(post => !existingIds.has(post.id))
      .slice(0, moreNeeded);
    
    return [...related, ...additional];
  }
  
  return related;
};